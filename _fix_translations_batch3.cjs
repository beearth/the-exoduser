// ─────────────────────────────────────────────────────────────────────────────
// Helper: read file
// ─────────────────────────────────────────────────────────────────────────────
function readFile(path) {
  return fs.readFileSync(path, 'utf8');
}

// ─────────────────────────────────────────────────────────────────────────────
// Helper: escape a value string for single-quoted JS
// ─────────────────────────────────────────────────────────────────────────────
function escVal(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

// ─────────────────────────────────────────────────────────────────────────────
// Helper: escape a key string for single-quoted JS
// ─────────────────────────────────────────────────────────────────────────────
function escKey(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

// ─────────────────────────────────────────────────────────────────────────────
// Check which keys are already present in a lang file
// ─────────────────────────────────────────────────────────────────────────────
function getMissingKeys(content, keys) {
  return keys.filter(k => {
    // Build regex that matches the key literally
    const escaped = k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    // match either 'key': or "key":
    return !new RegExp(`['"]${escaped}['"]\\s*:`).test(content);
  });
}

// ─────────────────────────────────────────────────────────────────────────────
// Inject missing entries just before the closing `};`
// ─────────────────────────────────────────────────────────────────────────────
function injectEntries(content, entries) {
  if (entries.length === 0) return content;
  const block = entries.map(([k, v]) => `  '${escKey(k)}': '${escVal(v)}'`).join(',\n');
  // Insert before the last `};`
  return content.replace(/\};\s*$/, `${block},\n};\n`);
}

// ─────────────────────────────────────────────────────────────────────────────
// Load missing_keys.json
// ─────────────────────────────────────────────────────────────────────────────
const missingRaw = JSON.parse(fs.readFileSync('G:/hell/_missing_keys.json', 'utf8'));
const missingJA  = new Set(missingRaw.ja  || []);
const missingZH  = new Set(missingRaw.zh  || []);
const missingZHT = new Set(missingRaw.zht || []);

// ─────────────────────────────────────────────────────────────────────────────
// Process each lang file
// ─────────────────────────────────────────────────────────────────────────────
const configs = [
  { path: 'G:/hell/lang_ja.js',  missingSet: missingJA,  idx: 0, label: 'JA'  },
  { path: 'G:/hell/lang_zh.js',  missingSet: missingZH,  idx: 1, label: 'ZH'  },
  { path: 'G:/hell/lang_zht.js', missingSet: missingZHT, idx: 2, label: 'ZHT' },
];

for (const { path, missingSet, idx, label } of configs) {
  let content = readFile(path);
  const entries = [];
  const noTranslation = [];

  for (const key of missingSet) {
    if (!T[key]) {
      noTranslation.push(key);
      continue;
    }
    entries.push([key, T[key][idx]]);
  }

  if (entries.length > 0) {
    content = injectEntries(content, entries);
    fs.writeFileSync(path, content, 'utf8');
    console.log(`[${label}] Added ${entries.length} entries to ${path}`);
  } else {
    console.log(`[${label}] No entries to add.`);
  }

  if (noTranslation.length > 0) {
    console.warn(`[${label}] WARNING: No translation for ${noTranslation.length} keys:`);
    noTranslation.forEach(k => console.warn(`  - ${JSON.stringify(k)}`));
  }
}

