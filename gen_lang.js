function generateFile(langCode, langUpper, langName, mainMap, pfxMap, baseMap) {
  let out = `// 지옥의 길 — ${langName}\n`;
  out += `const _${langUpper}={\n`;

  const entries = Object.entries(data.main);
  for (let i = 0; i < entries.length; i++) {
    const [k, en] = entries[i];
    const v = mainMap[k] || en;
    const kEsc = k.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    const vEsc = v.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    out += `'${kEsc}':'${vEsc}'`;
    if (i < entries.length - 1) out += ',';
    out += '\n';
  }
  out += '};\n';

  // PFX
  out += `const _${langUpper}_PFX={\n`;
  const pfxEntries = Object.entries(data.pfx);
  for (let i = 0; i < pfxEntries.length; i++) {
    const [k, en] = pfxEntries[i];
    const v = pfxMap[k] || en;
    const kEsc = k.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    const vEsc = v.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    out += `'${kEsc}':'${vEsc}'`;
    if (i < pfxEntries.length - 1) out += ',';
    out += '\n';
  }
  out += '};\n';

  // BASE
  out += `const _${langUpper}_BASE={\n`;
  const baseEntries = Object.entries(data.base);
  for (let i = 0; i < baseEntries.length; i++) {
    const [k, en] = baseEntries[i];
    const v = baseMap[k] || en;
    const kEsc = k.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    const vEsc = v.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    out += `'${kEsc}':'${vEsc}'`;
    if (i < baseEntries.length - 1) out += ',';
    out += '\n';
  }
  out += '};\n';

  return out;
}

