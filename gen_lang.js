function translateToAr(k, en) {
  const m = arMap[en];
  if (m !== undefined) return m;
  // Check if it starts with emoji
  const emojiMatch = en.match(/^([\p{Emoji_Presentation}\p{Emoji}\u200d\ufe0f\u20e3\u2600-\u27bf\u2b50\u2b55\u231a-\u23ff\u2702-\u27b0\u2934-\u2935\u25aa-\u25fe\u2190-\u21ff\u2300-\u23ff\u2460-\u24ff\u2500-\u25ff\u2700-\u27bf\u2900-\u297f\u2b00-\u2bff\u3000-\u303f\u3200-\u33ff]+\s?)/u);
  if (emojiMatch) {
    const prefix = emojiMatch[1];
    const rest = en.slice(prefix.length);
    const translated = arMap[rest];
    if (translated !== undefined) return prefix + translated;
  }
  return en; // fallback to English
}
