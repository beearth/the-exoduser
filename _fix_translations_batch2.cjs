// ─────────────────────────────────────────────────────────────
// Helper: load missing keys for a given lang code
// ─────────────────────────────────────────────────────────────
const missingData = JSON.parse(fs.readFileSync(
  path.join(__dirname, '_missing_keys.json'), 'utf8'));

// ─────────────────────────────────────────────────────────────
// Core: inject missing translations into a lang file
// ─────────────────────────────────────────────────────────────
function injectTranslations(filePath, langCode, translationMap) {
  const missingKeys = missingData[langCode] || [];
  if (missingKeys.length === 0) {
    console.log(`[${langCode.toUpperCase()}] No missing keys — skipping.`);
    return;
  }

  let src = fs.readFileSync(filePath, 'utf8');

  // Find insertion point: just before closing `};`
  const closingIdx = src.lastIndexOf('};');
  if (closingIdx === -1) {
    console.error(`[${langCode.toUpperCase()}] Could not find closing }; in ${filePath}`);
    return;
  }

  // Load existing keys by parsing the object naively
  // We'll just check if the raw key string appears already
  const existingKeysSet = new Set();
  // Match all single-quoted keys in the file
  const keyRe = /'((?:[^'\\]|\\.)*)'\s*:/g;
  let m;
  while ((m = keyRe.exec(src)) !== null) {
    existingKeysSet.add(m[1]);
  }

  const lines = [];
  let addedCount = 0;
  let skippedCount = 0;
  let unknownCount = 0;

  for (const koKey of missingKeys) {
    if (existingKeysSet.has(koKey)) {
      skippedCount++;
      continue;
    }
    if (!translationMap.hasOwnProperty(koKey)) {
      console.warn(`  [${langCode.toUpperCase()}] WARN: no translation for: ${koKey}`);
      unknownCount++;
      continue;
    }
    const val = translationMap[koKey];
    // Escape single quotes in key and value
    const escapedKey = koKey.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    const escapedVal = val.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    lines.push(`  '${escapedKey}':'${escapedVal}',`);
    addedCount++;
  }

  if (lines.length === 0) {
    console.log(`[${langCode.toUpperCase()}] All missing keys already present or unmapped.`);
    return;
  }

  const injection = '\n// --- batch2 additions ---\n' + lines.join('\n') + '\n';
  const newSrc = src.slice(0, closingIdx) + injection + src.slice(closingIdx);
  fs.writeFileSync(filePath, newSrc, 'utf8');
  console.log(`[${langCode.toUpperCase()}] Done. Added: ${addedCount}, Skipped (existed): ${skippedCount}, Unknown: ${unknownCount}`);
}

