// ─── Load missing keys ────────────────────────────────────────────────────────
const missing = require('./_missing_keys.json');

// ─── Process each file ────────────────────────────────────────────────────────
let totalAdded = 0;

for (const { file, lang, varName } of FILES) {
  const keys = missing[lang] || [];
  if (keys.length === 0) {
    console.log(`[${lang}] No missing keys, skipping.`);
    continue;
  }

  let src = fs.readFileSync(file, 'utf8');

  // Find the closing `};` of the MAIN object (last occurrence)
  // We insert BEFORE it
  const closeIdx = src.lastIndexOf('\n};');
  if (closeIdx === -1) {
    console.error(`[${lang}] ERROR: could not find closing \\n}; in ${file}`);
    continue;
  }

  // Build the new entries
  const lines = [];
  let added = 0;
  const notFound = [];

  for (const key of keys) {
    const row = T[key];
    if (!row) {
      notFound.push(key);
      continue;
    }
    const val = row[lang];
    if (val === undefined) {
      notFound.push(key);
      continue;
    }
    // Escape single quotes in key and value
    const safeKey = key.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    const safeVal = val.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    lines.push(`  '${safeKey}': '${safeVal}',`);
    added++;
  }

  if (lines.length === 0 && notFound.length === 0) {
    console.log(`[${lang}] Nothing to add.`);
    continue;
  }

  if (lines.length > 0) {
    const block = '\n  // --- batch4 additions ---\n' + lines.join('\n') + '\n';
    src = src.slice(0, closeIdx) + block + src.slice(closeIdx);
    fs.writeFileSync(file, src, 'utf8');
    console.log(`[${lang}] Added ${added} keys to ${file}`);
    totalAdded += added;
  }

  if (notFound.length > 0) {
    console.warn(`[${lang}] ${notFound.length} keys NOT in translation table:`);
    notFound.forEach(k => console.warn(`  - ${JSON.stringify(k)}`));
  }
}

console.log(`\nDone. Total added: ${totalAdded}`);
