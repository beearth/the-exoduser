// Parse all KV pairs from a file, returning a Map (key -> value), last-seen wins
function parseAllKV(content) {
  const KV_RE = /'((?:[^'\\]|\\.)*)'\s*:\s*'((?:[^'\\]|\\.)*)'/g;
  const map = new Map();
  const order = [];
  let m;
  while ((m = KV_RE.exec(content)) !== null) {
    const k = m[1];
    const v = m[2];
    if (!map.has(k)) order.push(k);
    map.set(k, v);
  }
  return { map, order };
}

// Find the end of the original _XX dict (first `};` in the file)
function findFirstDictEnd(lines) {
  let inDict = false;
  for (let i = 0; i < lines.length; i++) {
    if (!inDict && /^const _[A-Z]+\s*=\s*\{/.test(lines[i])) inDict = true;
    if (inDict && /^\};/.test(lines[i])) return i;
  }
  return -1;
}

function escKey(s) { return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'"); }
function escVal(s) { return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'"); }

function processFile(filePath, lang) {
  const content = fs.readFileSync(filePath, 'utf8');
  const lines   = content.split('\n');

  // Find where first dict ends
  const dictEndLine = findFirstDictEnd(lines);
  if (dictEndLine === -1) { console.error(`Cannot find dict end in ${filePath}`); return; }

  // Extract everything before the first `};` (the dict body, excluding the opening const line)
  const headerLine = lines.findIndex(l => /^const _[A-Z]+\s*=\s*\{/.test(l));
  const dictBodyLines = lines.slice(headerLine + 1, dictEndLine);

  // Everything after (and including) the first `};`
  const afterDict = lines.slice(dictEndLine); // includes `};`, _JA_PFX, _JA_BASE etc

  // But afterDict may contain injected block entries (from prior bad runs) embedded in _JA_BASE
  // We need to strip those injected entries from afterDict too.
  // Strategy: rebuild afterDict by finding the start of _JA_PFX/_ZH_PFX/_ZHT_PFX and restoring it
  // The injected entries are block-style (^  'key': 'val',) embedded inside _JA_BASE

  // Find where _JA_BASE (or _ZH_BASE etc) inline section ends (last inline line before block entries)
  // The inline section uses the format: 'key':'val','key':'val' (no leading spaces)
  // The injected entries have `  'key': 'val',` (leading spaces)

  // We'll rebuild afterDict by removing block-style entries
  // that are NOT part of the original structure
  const cleanAfterDict = afterDict.map(line => {
    // block-style injected entries: "  'Korean key': 'Translation',"
    // original _JA_BASE entries: "'Korean':'Japanese'" (no leading whitespace)
    // We keep lines that are NOT block-style translation entries
    // A block-style entry matches: starts with spaces, then 'key': 'val',
    if (/^\s{2,}'[^']*'\s*:\s*'[^']*',\s*$/.test(line)) {
      // Check if this looks like an injected entry (Korean key)
      const km = line.match(/^\s*'([^'\\]*(?:\\.[^'\\]*)*)'/);
      if (km) {
        const k = km[1];
        // If this key is a Korean string, it's an injected entry, skip it
        if (/[\uAC00-\uD7A3\u4E00-\u9FFF]/.test(k) || /^[🌊🪞💢⚡💣💀💥💫🔷🔥🌋⛧🟢❄☠⚔⚠🌑🌀👹🖐🛡⚓🩸🕷✟👻🦴🔨🛡️]/.test(k)) {
          return null; // remove
        }
      }
    }
    return line;
  }).filter(l => l !== null);

  // Now parse all KV from the dictBody (the main dict)
  const bodyContent = dictBodyLines.join('\n');
  const { map: kvMap, order: kvOrder } = parseAllKV(bodyContent);

  // Apply authoritative batch3 translations
  const b3 = BATCH3[lang] || {};
  for (const [k, v] of Object.entries(b3)) {
    if (!kvMap.has(k)) kvOrder.push(k); // add new keys
    kvMap.set(k, v);
  }

  // Check for any keys in the afterDict injected blocks that aren't in the main dict
  // (they should all be in batch3 now)
  const afterContent = afterDict.join('\n');
  const { map: afterMap } = parseAllKV(afterContent);
  let addedFromAfter = 0;
  for (const [k, v] of afterMap) {
    // Only consider Korean keys
    if (/[\uAC00-\uD7A3]/.test(k) && !kvMap.has(k) && !b3[k]) {
      kvOrder.push(k);
      kvMap.set(k, v);
      addedFromAfter++;
    }
  }

  // Rebuild comment lines from dictBody (preserve section comments)
  const commentLines = dictBodyLines.filter(l => /^\s*\/\//.test(l));

  // Build new dict body: section comments + all KV pairs
  const kvLines = kvOrder.map(k => {
    const v = kvMap.get(k);
    return `  '${escKey(k)}': '${escVal(v)}',`;
  });

  // Reconstruct the file
  const dictHeader = lines[headerLine];
  const newContent = [
    ...lines.slice(0, headerLine),
    dictHeader,
    ...commentLines,
    ...kvLines,
    ...cleanAfterDict,
  ].join('\n');

  fs.writeFileSync(filePath, newContent, 'utf8');
  console.log(`[${lang.toUpperCase()}] Rewrote ${filePath}`);
  console.log(`  Keys in main dict: ${kvMap.size}`);
  console.log(`  Added from stale blocks: ${addedFromAfter}`);
}

