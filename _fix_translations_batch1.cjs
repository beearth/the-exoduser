const LANGS = [
  { code: 'bg', idx: 0, file: path.join(BASE, 'lang_bg.js') },
  { code: 'cs', idx: 1, file: path.join(BASE, 'lang_cs.js') },
  { code: 'da', idx: 2, file: path.join(BASE, 'lang_da.js') },
];

const missing = require(path.join(BASE, '_missing_keys.json'));

for (const lang of LANGS) {
  const needed = missing[lang.code];
  if (!needed || needed.length === 0) {
    console.log(`[${lang.code}] Nothing missing, skip.`);
    continue;
  }

  let src = fs.readFileSync(lang.file, 'utf8');

  const lines = [];
  let found = 0, notInTable = [];

  for (const key of needed) {
    if (!T[key]) {
      notInTable.push(key);
      continue;
    }
    const val = T[key][lang.idx];
    // Escape single quotes in key and value
    const escapedKey = key.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    const escapedVal = val.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    lines.push(`'${escapedKey}':'${escapedVal}'`);
    found++;
  }

  if (lines.length > 0) {
    const insertion = lines.join(',\n') + ',\n';
    // Insert before the closing };
    src = src.replace(/\};\s*$/, insertion + '};\n');
    fs.writeFileSync(lang.file, src, 'utf8');
    console.log(`[${lang.code}] Added ${found} keys.`);
  }

  if (notInTable.length > 0) {
    console.log(`[${lang.code}] WARNING — ${notInTable.length} keys not in translation table:`);
    notInTable.forEach(k => console.log(`  - "${k}"`));
  }
}

console.log('Done.');
